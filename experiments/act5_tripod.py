def _compute_epv(windows_df: pd.DataFrame, cohort: str, act1_results: dict | None = None) -> pd.DataFrame:
    """Compute Events Per Variable (EPV) per event type for both GLMM variants.

    Reports EPV for:
      - Full GLMM (40 features, sensitivity analysis)
      - Parsimonious GLMM (8 features, primary model) — from act1_results if available

    EPV = n_event_windows / n_predictors.
    Guideline: EPV ≥ 10 considered minimum; < 10 flagged as limitation.

    Returns DataFrame with columns:
      cohort, event_type, model, n_events, n_features, epv, flag.
    """
    from experiments.act1_clinic import PARSIMONIOUS_FEATURES
    feat_cols = get_feature_cols(windows_df)
    n_features_full = len(feat_cols)
    rows = []
    for etype in EVENT_TYPES:
        sub = windows_df[windows_df["event_type"] == etype]
        n_events = int((sub["label"] == 1).sum())

        # Full GLMM (40 features)
        epv_full = n_events / n_features_full if n_features_full > 0 else float("nan")
        rows.append({
            "cohort": cohort,
            "event_type": etype,
            "model": "GLMM_full_40feat",
            "n_events": n_events,
            "n_features": n_features_full,
            "epv": round(epv_full, 2),
            "flag": "LOW EPV (<10)" if epv_full < 10 else "OK",
        })

        # Parsimonious GLMM (pre-specified features from Act 4 robustness)
        pars_feats = PARSIMONIOUS_FEATURES.get(etype, [])
        if act1_results and etype in act1_results:
            # Use the actual feature list used in Act 1 (may differ if some cols missing)
            pars_feats = act1_results[etype].get("glmm_pars_features", pars_feats)
        n_features_pars = len(pars_feats)
        if n_features_pars > 0:
            epv_pars = n_events / n_features_pars
            rows.append({
                "cohort": cohort,
                "event_type": etype,
                "model": "GLMM_parsimonious_8feat",
                "n_events": n_events,
                "n_features": n_features_pars,
                "epv": round(epv_pars, 2),
                "flag": "LOW EPV (<10)" if epv_pars < 10 else "OK",
            })

    return pd.DataFrame(rows)


def _save_table1_for_cohort(
    windows_df: pd.DataFrame,
    cohort: str,
) -> pd.DataFrame:
    """Return a per-cohort Table 1 DataFrame."""
    feat_cols = get_feature_cols(windows_df)
    rows = []
    for etype in EVENT_TYPES:
        sub = windows_df[windows_df["event_type"] == etype]
        if len(sub) == 0:
            continue
        n_patients = (
            sub["patient_id"].nunique() if "patient_id" in sub.columns else None
        )
        n_events = int((sub["label"] == 1).sum())
        n_ctrl = int((sub["label"] == 0).sum())
        row: dict = {
            "cohort": cohort,
            "event_type": etype,
            "n_windows": len(sub),
            "n_events": n_events,
            "n_controls": n_ctrl,
            "prevalence": round(n_events / max(len(sub), 1), 3),
            "n_patients": n_patients,
        }
        for feat in feat_cols:
            vals = sub[feat].dropna()
            row[f"{feat}_mean"] = round(float(vals.mean()), 4) if len(vals) else None
            row[f"{feat}_sd"] = round(float(vals.std()), 4) if len(vals) else None
        rows.append(row)
    return pd.DataFrame(rows)


def run_act5() -> dict:
    """Generate TRIPOD checklist and merged Table 1."""
    logger.info("=== ACT 5: TRIPOD Checklist & Table 1 ===")

    # ------------------------------------------------------------------ #
    # 1. Load previous results (best-effort — missing acts are skipped)
    # ------------------------------------------------------------------ #
    prior_results: dict[str, dict] = {}
    for act_name, path in RESULTS_PATHS.items():
        if path.exists():
            with open(path) as fh:
                prior_results[act_name] = json.load(fh)
            logger.info("  Loaded %s results.", act_name)
        else:
            logger.info("  %s results not found (%s) — skipping.", act_name, path)

    # ------------------------------------------------------------------ #
    # 1b. Compute EPV (Events Per Variable) for development cohort
    # ------------------------------------------------------------------ #
    epv_df_all: list[pd.DataFrame] = []
    clinic_cache = CACHE_DIR / COHORT_CACHE.get("clinic", "clinic_windows.parquet")
    if clinic_cache.exists():
        clinic_windows = pd.read_parquet(clinic_cache)
        act1_res = prior_results.get("act1")
        epv_df = _compute_epv(clinic_windows, "clinic", act1_results=act1_res)
        epv_df_all.append(epv_df)
        epv_csv = OUT_DIR / "epv_by_event_type.csv"
        epv_df.to_csv(epv_csv, index=False)
        logger.info("EPV (clinic):\n%s", epv_df.to_string(index=False))

        # Update TRIPOD item 8 with computed EPV values (primary = parsimonious model)
        pars_df = epv_df[epv_df["model"].str.contains("parsimonious")]
        full_df  = epv_df[epv_df["model"].str.contains("full")]
        pars_summary = "; ".join(
            f"{r['event_type']}: EPV {r['epv']} (n_events={r['n_events']}, n_feat={r['n_features']}) [{r['flag']}]"
            for _, r in pars_df.iterrows()
        ) if not pars_df.empty else "(parsimonious model not yet run)"
        full_summary = "; ".join(
            f"{r['event_type']}: EPV {r['epv']} [{r['flag']}]"
            for _, r in full_df.iterrows()
        )
        for item in TRIPOD_CHECKLIST:
            if item["item"] == "8":
                item["evidence"] = (
                    "Sample size constrained by available ICU recordings. "
                    "PRIMARY MODEL — Parsimonious GLMM (8 pre-specified domain-robust features): "
                    f"{pars_summary}. "
                    "Hypertension EPV still < 10 — reported as limitation. "
                    "SENSITIVITY ANALYSIS — Full GLMM (40 features): "
                    f"{full_summary}. "
                    "Regularisation via BayesMixedGLM partial pooling; coefficient uncertainty CIs reported."
                )
                # Item addressed if PRIMARY (parsimonious) EPV meets criteria or is declared limitation
                item["addressed"] = "Yes" if not pars_df.empty else "Partial"
                break

    # Annotate TRIPOD items with actual AUC numbers where available
    if "act1" in prior_results:
        for item in TRIPOD_CHECKLIST:
            if item["item"] == "15a":
                item["evidence"] += f"  Hypotension GLMM AUC={prior_results['act1'].get('hypotension', {}).get('glmm_cv_auc_mean', 'n/a'):.3f}."
            if item["item"] == "16" and "act2" in prior_results:
                item["evidence"] += (
                    f"  MIMIC GLMM hypotension AUC={prior_results['act2'].get('hypotension', {}).get('glmm_auc', 'n/a')}."
                )

    tripod_path = OUT_DIR / "tripod_checklist.json"
    with open(tripod_path, "w") as fh:
        json.dump(TRIPOD_CHECKLIST, fh, indent=2, ensure_ascii=False)
    logger.info("TRIPOD checklist saved: %s (%d items)", tripod_path, len(TRIPOD_CHECKLIST))

    # Also save as CSV for easy review
    pd.DataFrame(TRIPOD_CHECKLIST).to_csv(OUT_DIR / "tripod_checklist.csv", index=False)

    # ------------------------------------------------------------------ #
    # 2. Merged Table 1 across all available cohorts
    # ------------------------------------------------------------------ #
    table1_parts: list[pd.DataFrame] = []
    for cohort, fname in COHORT_CACHE.items():
        cache_path = CACHE_DIR / fname
        if not cache_path.exists():
            logger.info("  Table 1: %s cache not found, skipping.", cohort)
            continue
        logger.info("  Building Table 1 for %s...", cohort)
        windows_df = pd.read_parquet(cache_path)
        t1 = _save_table1_for_cohort(windows_df, cohort)
        table1_parts.append(t1)
        t1.to_csv(OUT_DIR / f"table1_{cohort}.csv", index=False)

    if table1_parts:
        merged = pd.concat(table1_parts, ignore_index=True)
        merged.to_csv(OUT_DIR / "table1_all_cohorts.csv", index=False)
        logger.info("  Merged Table 1 saved: %d rows", len(merged))

        # Print concise version
        _cols = ["cohort", "event_type", "n_patients", "n_windows", "n_events", "prevalence"]
        _present = [c for c in _cols if c in merged.columns]
        logger.info("\n%s", merged[_present].to_string(index=False))

    # ------------------------------------------------------------------ #
    # 3. Summary JSON
    # ------------------------------------------------------------------ #
    summary = {
        "tripod_items_total": len(TRIPOD_CHECKLIST),
        "tripod_fully_addressed": sum(1 for i in TRIPOD_CHECKLIST if i["addressed"] == "Yes"),
        "tripod_partial": sum(1 for i in TRIPOD_CHECKLIST if i["addressed"] == "Partial"),
        "acts_available": list(prior_results.keys()),
        "cohorts_in_table1": [c for c in COHORT_CACHE if (CACHE_DIR / COHORT_CACHE[c]).exists()],
    }
    with open(OUT_DIR / "act5_results.json", "w") as fh:
        json.dump(summary, fh, indent=2)

    logger.info(
        "Act 5 complete. TRIPOD: %d/22 fully addressed, %d partial.",
        summary["tripod_fully_addressed"],
        summary["tripod_partial"],
    )
    return summary


if __name__ == "__main__":
    run_act5()
